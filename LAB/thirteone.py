text = "PythonProgramming"

# Access first 6 characters
print("First 6 characters:", text[:6])

# Access characters from index 6 to end
print("From index 6 to end:", text[6:])

# Access characters from index 2 to 8
print("From index 2 to 8:", text[2:9])

# Every 2nd character
print("Every 2nd character:", text[::2])

# Reverse the string
print("Reversed string:", text[::-1])
