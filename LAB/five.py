"""
This program demonstrates how to create different types of variables in Python.
"""

# Creating variables

# Integer variable
age = 20

# Float variable
height = 5.7

# String variable
name = "Matin"

# Boolean variable
is_student = True

# List variable
fruits = ["apple", "banana", "mango"]

# Tuple variable
colors = ("red", "green", "blue")

# Dictionary variable
student = {
    "name": "Matin",
    "age": 20,
    "course": "Python"
}

# Set variable
unique_numbers = {1, 2, 3, 3, 4}

# Printing variables
print("Age:", age)
print("Height:", height)
print("Name:", name)
print("Is Student:", is_student)
print("Fruits:", fruits)
print("Colors:", colors)
print("Student Info:", student)
print("Unique Numbers:", unique_numbers)
